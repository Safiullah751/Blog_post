    {
         //Vlidate
        $fildes = $request->validate([
           'title'=>['required' ,'max:100' ],
           'body'=>['required' , 'max:2300']
        ]);
        //create
        Auth::user()->posts()->create($fildes);

        return redirect()->route('posts.index')-> with('success' ,'post Successfully added');
    }

